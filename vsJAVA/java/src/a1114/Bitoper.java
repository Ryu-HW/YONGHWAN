package a1114;

public class Bitoper {
    public static void main(String[] args) {
        System.out.println("45 & 25 = " + (45 & 25)); //논리곱
        System.out.println("45 | 25 = " + (45 | 25)); //논리합
        System.out.println("45 ^ 25 = " + (45 ^ 25)); //배타적 논리합
        System.out.println("~45 = " + (~45)); //논리 부정
    }
}
